package vegetables;

public class Vegetable {

    protected String name;
    protected int calorie;
    protected int cost;
    protected int weight;

    public Vegetable() {
    }

    public String getName() {
        return name;
    }

    public double getCalorie() {
        return calorie;
    }

    public int getCost() {
        return cost;
    }

    public int getWeight() {
        return weight;
    }

}
